<?php
class Indirizzo{

    private $indirizzo;
    private $numCiv;

    public function __construct($indirizzo,$numCiv){
        $this->indirizzo;
        $this->$numCiv;
    }

    public function setIndirizzo($indirizzo):void{
        $this->indirizzo;
    }

    public function getIndirizzo():string{
        return $this->indirizzo;
    }

    public function setNumCiv($numCiv):void{
        $this->numCiv;
    }

    public function getNumCiv():string{
        return $this->numCiv;
    }

    
}